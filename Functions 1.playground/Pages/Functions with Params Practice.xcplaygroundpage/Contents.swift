//: ### Exercise 1
//: Define a function called `loveIt` that takes 1 `String` parameter called `subject` and prints "I ❤️ [subject]".
//: **The solution is available on the next page!**

func loveIt (subject: String) {
    print ("I ❤️ \(subject)")
}

loveIt("apple")

